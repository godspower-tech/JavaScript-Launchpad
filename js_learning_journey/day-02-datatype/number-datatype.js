// Variables: Declaring and assigning values to variables.

// Declaring variables with let (for values that may change) and const (for constants).
let age = 35; // Age (can change over time).
const gravity = 9.81; // Gravitational constant (m/s^2).
let mass = 72; // Mass (in kilograms).
const PI = 3.14; // Mathematical constant PI.

// More examples of constants.
const boilingPoint = 100; // Boiling point of water (in degrees Celsius).
const bodyTemp = 37; // Average human body temperature (in degrees Celsius).

// Output the values of all variables.
console.log(age, gravity, mass, PI, boilingPoint, bodyTemp);