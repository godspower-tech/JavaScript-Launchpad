// replace(): Replaces a specified substring with a new substring.
// Syntax: string.replace(oldSubstring, newSubstring)
// Returns: A new string with the replaced substring.

let string = 'Js Learning JavaScript';

// Replace 'JavaScript' with 'Journey'.
console.log(string.replace('JavaScript', 'Journey')); // Output: Js Learning Journey

let country = 'Finland';

// Replace 'Fin' with 'Noman'.
console.log(country.replace('Fin', 'Noman')); // Output: Nomanland