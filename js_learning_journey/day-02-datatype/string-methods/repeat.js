// repeat(): Creates a new string by repeating the original string a specified number of times.
// Syntax: string.repeat(count)
// Returns: A new string containing the repeated original string.

let string = 'JavaScript-';

// Repeat the string 'JavaScript-' 10 times.
console.log(string.repeat(10)); // Output: JavaScript-JavaScript-JavaScript-JavaScript-JavaScript-JavaScript-JavaScript-JavaScript-JavaScript-JavaScript-