// search(): Searches a string for a specified substring and returns the index of the first match.
// Syntax: string.search(substring)
// Returns: The index of the first match, or -1 if no match is found.

let string = 'I love JavaScript. If you do not love JavaScript what else can you love.';

// Find the index of the first occurrence of 'love'.
console.log(string.search('love')); // Output: 2