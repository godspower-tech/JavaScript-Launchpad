// concat(): Combines multiple strings into a single string.
// Syntax: string.concat(substring1, substring2, ..., substringN)

let string = 'Js';

// Concatenate "Learning", "Journey" to the original string.
console.log(string.concat(" Learning", "Journey")); // Output: Js Learning Journey

let country = 'Fin';

// Concatenate "land" to the original string.
console.log(country.concat("land")); // Output: Finland