// substr(): Extracts a substring from a string, starting at a specified index and returning a specified number of characters.
// Syntax: string.substr(startIndex, length)
// Returns: The extracted substring.

let string = 'JavaScript';

// Extract 6 characters starting from index 4.
console.log(string.substr(4, 6)); // Output: Script

let country = 'Finland';

// Extract 4 characters starting from index 3.
console.log(country.substr(3, 4)); // Output: land