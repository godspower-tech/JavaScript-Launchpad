// charAt(): Retrieves the character at a specified index within a string.
// Syntax: string.charAt(index)

let string = 'Js Learning Journey';

// Get the character at index 0 (the first character).
console.log(string.charAt(0)); // Output: 3

// Calculate the index of the last character in the string.
let lastIndex = string.length - 1;

// Get the character at the last index.
console.log(string.charAt(lastIndex)); // Output: t