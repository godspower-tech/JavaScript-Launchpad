// trim(): Removes leading and trailing whitespace from a string.
// Syntax: string.trim()
// Returns: A new string with whitespace removed from both ends.

let string = '    Js Learning Journey    ';

// Display the original string with leading and trailing spaces.
console.log(string); // Output: "    Js Learning Journey    "

// Remove leading and trailing spaces using trim().
console.log(string.trim()); // Output: "Js Learning Journey"

let firstName = ' Asabeneh ';

// Display the original string with leading and trailing spaces.
console.log(firstName); // Output: " Asabeneh "

// Remove leading and trailing spaces using trim().
console.log(firstName.trim()); // Output: "Asabeneh"